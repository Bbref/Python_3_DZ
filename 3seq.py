# Запрашиваем ввод элементов первого списка
user_input1 = input("Введите элементы 1-го списка, разделенные запятыми: ")
list1 = user_input1.split(',')
list1 = list(map(int, list1))  # Преобразуем элементы в целые числа

# Запрашиваем ввод элементов второго списка
user_input2 = input("Введите элементы 2-го списка, разделенные запятыми: ")
list2 = user_input2.split(',')
list2 = list(map(int, list2))  # Преобразуем элементы в целые числа

# Удаляем элементы второго списка из первого
result = [item for item in list1 if item not in list2]

# Преобразуем результат в строку, разделенную запятыми
result_str = ','.join(map(str, result))

# Выводим результат
print("Результат:", result_str)
